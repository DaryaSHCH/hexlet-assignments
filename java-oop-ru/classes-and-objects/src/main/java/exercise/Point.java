package exercise;

public class Point {
    private int X;
    private int Y;

    public Point(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }

    public Point() {
        X = 0;
        Y = 0;
    }

    public int getX() {
        return this.X;
    }


    public int getY() {
        return this.Y;
    }

}
